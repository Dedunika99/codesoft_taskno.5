    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
       try {
        String courseIDInput = JOptionPane.showInputDialog("Enter Student ID");

        if (courseIDInput != null && !courseIDInput.isEmpty()) {
            searchstudentId = courseIDInput;

            String query = "SELECT student_name,reg_course FROM student WHERE student_id=?";
            pst = db.con.prepareStatement(query);

            pst.setString(1, searchstudentId);
            rs = pst.executeQuery();
        

                if (rs.next()) {


                    
                    name.setText(rs.getString("student_name"));
                    course.setText(rs.getString("reg_course"));
                   

                } else {
                    JOptionPane.showMessageDialog(null, "Student not found");
                }
            } else {
                JOptionPane.showMessageDialog(null, "Please enter a valid Student id");
            }
        } catch (NumberFormatException | SQLException ex) {
            JOptionPane.showMessageDialog(null, "Invalid Student format");
            Logger.getLogger(courseRegistration.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

