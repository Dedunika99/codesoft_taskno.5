    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String sid = id.getText();
        String sname = name.getText();
        String scourse = course.getText();

        try {
            pst = db.con.prepareStatement("INSERT INTO student(student_id,student_name,reg_course)VALUES(?,?,?)");
            pst.setString(1, sid);
            pst.setString(2, sname);
            pst.setString(3, scourse);

            int k = pst.executeUpdate();

            if (k == 1) {
                JOptionPane.showMessageDialog(this, "Record added");
            } else {
                JOptionPane.showMessageDialog(this, "Error");
            }
        } catch (SQLException ex) {
            Logger.getLogger(courseRegistration.class.getName()).log(Level.SEVERE, null, ex);
        }

        id.setText("");
        name.setText("");
        course.setText("");
        
    }//GEN-LAST:event_jButton1ActionPerformed

